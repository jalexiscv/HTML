namespace App\Libraries\Html\Bootstrap;
/**
 * Representa un nodo individual dentro de un árbol.
 * Cada nodo puede contener un valor y tiene la capacidad de tener nodos hijos,
 * permitiendo la creación de estructuras de árbol complejas.
 */
class TreeNode
{
    /**
     * El valor del nodo. Puede ser de cualquier tipo, dependiendo del uso del árbol.
     */
    public $value;
    /**
     * Array de nodos hijos. Está inicializada como un array vacío y
     * puede llenarse con instancias de TreeNode.
     */
    public $children = [];

    public $title = false;

    /**
     * Constructor de la clase TreeNode.
     * @param mixed $value El valor del nodo.
     */
    public function __construct(mixed $value, $title = false)
    {
        $this->value = $value;
        if ($title) {
            $this->title = $title;
        }
    }

    /**
     * Añade un nodo hijo a este nodo.
     * @param TreeNode $node Instancia del nodo hijo a añadir.
     */
    public function addChild(TreeNode $node)
    {
        $this->children[] = $node;

    }
}